package com.netease.explore.shopping.domain;


/**
 * 用户
 */
public class User {

  private String id;

  /**
   * 用户名
   */
  private String name;

  /**
   * get、set方法
   */
  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }
}
